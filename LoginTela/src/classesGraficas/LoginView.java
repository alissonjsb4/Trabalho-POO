    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        try {
            String login = campoLogin.getText();
            String senha = campoSenha.getText();

            if (login.isEmpty() || senha.isEmpty()) {
                throw new LoginException(login, senha);
            }

            final String ADMIN_LOGIN = "AdminCinema";
            final String ADMIN_SENHA = "123AdminCinema789";

            if (login.equals(ADMIN_LOGIN) && senha.equals(ADMIN_SENHA)) {
                abrirTelaAdministrador();
            } else {
                buscarUsuarioEExibirMenu(login, senha);
            }
        } catch (LoginException ex) {
            exibirMensagemDeErro(ex.getMessage());
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed
    
    private void abrirTelaAdministrador() {
        ADMView telaADM = new ADMView();
        this.setVisible(false);
        telaADM.setVisible(true);
    }
    
    private void buscarUsuarioEExibirMenu(String login, String senha) throws LoginException {
        UsuarioAbstrato[] usuarios = new UsuarioVector().listar();
        boolean checador = true;

        for (UsuarioAbstrato usuario : usuarios) {
            if (login.equals(usuario.getLogin())) {
                if (senha.equals(usuario.getSenha())) {
                    abrirTelaMenu(usuario.getLogin(), usuario.getNome(), usuario.getIdade(), usuario.getCredito());
                    checador = false;
                    break;
                } else {
                    throw new LoginException("Senha incorreta! ");
                }
            }
        }
        if (checador) {
            throw new LoginException("Login inexistente!");
        }
    }

    private void abrirTelaMenu(String login, String nome, int idade, double credito) {
        MenuInicial telaMenu = new MenuInicial(login, nome, idade, credito);
        this.setVisible(false);
        telaMenu.setVisible(true);
    }

    private void exibirMensagemDeErro(String mensagem) {
        ErroView erro = new ErroView(mensagem);
        erro.setVisible(true);
    }
    
